#!/bin/bash
#功能描述(Description):对比${!字串*}与${!字串@}的区别.

echo "-----------------------------"
echo -e "\033[32m*将被扩展为一个整体,循环1次结束.\033[0m"
for i in "${!U*}"
do
    echo "变量名称为:$i"
done
echo
echo "-----------------------------"
echo -e "\033[32m@将被扩展为独立的单词,循环n次结束.\033[0m"
for i in "${!U@}"
do
    echo -e "变量名称为:$i"
done
echo "-----------------------------"
